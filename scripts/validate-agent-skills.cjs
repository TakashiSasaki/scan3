const fs = require('fs');
const path = require('path');

const registryPath = path.join(__dirname, '../reconstruction/agent-skills.json');
const inventoryPath = path.join(__dirname, '../reconstruction/accepted-artifacts.json');

const registry = JSON.parse(fs.readFileSync(registryPath, 'utf8'));
const inventory = JSON.parse(fs.readFileSync(inventoryPath, 'utf8'));

const requiredSections = [
  'Name', 'Status', 'Purpose', 'Preconditions', 'Authoritative References',
  'Inputs', 'Procedure', 'Decision Gates', 'Stop Conditions',
  'Verification', 'Outputs', 'Non-Goals'
];

let failed = false;

for (const skill of registry.skills) {
  const dirPath = path.join(__dirname, '..', skill.directory);
  if (!fs.existsSync(dirPath)) {
    console.error(`Skill directory missing: ${skill.directory}`);
    failed = true;
    continue;
  }
  const skillMdPath = path.join(dirPath, 'SKILL.md');
  if (!fs.existsSync(skillMdPath)) {
    console.error(`SKILL.md missing in ${skill.directory}`);
    failed = true;
    continue;
  }
  
  const content = fs.readFileSync(skillMdPath, 'utf8');
  if (!content.includes('Repository-local handwritten skill.') || !content.includes('Not generated by agent-policy.')) {
    console.error(`Missing handwritten marker in ${skill.name}`);
    failed = true;
  }
  if (content.includes('generated by agent-policy') && !content.includes('Not generated by agent-policy')) {
    console.error(`Claims to be generated by agent-policy in ${skill.name}`);
    failed = true;
  }
  
  for (const sec of requiredSections) {
    if (!content.includes(`# ${sec}`)) {
      console.error(`Missing required section '# ${sec}' in ${skill.name}`);
      failed = true;
    }
  }
  
  // Authoritative References: check for policy or script
  const refMatch = content.match(/# Authoritative References\n([\s\S]*?)# Inputs/);
  if (!refMatch || (!refMatch[1].includes('policy/') && !refMatch[1].includes('script'))) {
    console.error(`Authoritative References must include policy or script in ${skill.name}`);
    failed = true;
  }
  
  const relativeSkillMd = path.posix.join(skill.directory, 'SKILL.md');
  if (!inventory.includes(relativeSkillMd)) {
    console.error(`Skill SKILL.md not in accepted artifacts inventory: ${relativeSkillMd}`);
    failed = true;
  }
}

// Check for unregistered scan3-* skills
const skillsRoot = path.join(__dirname, '../.agents/skills');
if (fs.existsSync(skillsRoot)) {
  const dirs = fs.readdirSync(skillsRoot);
  for (const dir of dirs) {
    if (dir.startsWith('scan3-') && fs.statSync(path.join(skillsRoot, dir)).isDirectory()) {
      if (!registry.skills.find(s => s.name === dir)) {
        console.error(`Unregistered skill directory found: ${dir}`);
        failed = true;
      }
    }
  }
}

if (failed) {
  console.error('Agent skills validation failed.');
  process.exit(1);
} else {
  console.log('Agent skills validation passed.');
}
